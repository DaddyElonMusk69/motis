class Order(TimestampMixin, Base):
    """Order generated by an operator. May or may not be filled."""
    __tablename__ = "orders"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    operator_id = Column(UUID(as_uuid=True), ForeignKey("operators.id", ondelete="CASCADE"), nullable=False, index=True)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)

    exchange = Column(String(32), nullable=False)
    symbol = Column(String(32), nullable=False)
    side = Column(String(8), nullable=False)         # 'buy' | 'sell'
    order_type = Column(String(16), nullable=False)  # 'market' | 'limit' | 'stop_market' | 'stop_limit'
    size = Column(Float, nullable=False)
    price = Column(Float, nullable=True)             # None for market orders
    stop_price = Column(Float, nullable=True)
    reduce_only = Column(Boolean, nullable=False, server_default="false")
    is_paper = Column(Boolean, nullable=False, server_default="false")
    client_order_id = Column(String(64), unique=True, nullable=False)

    # Exchange-side status
    exchange_order_id = Column(String(128), nullable=True)
    status = Column(String(32), nullable=False, server_default="pending")  # pending|submitted|filled|cancelled|rejected

    # Relationships
    operator = relationship("Operator", back_populates="orders")
    fill = relationship("TradeLog", back_populates="order", uselist=False)


class TradeLog(Base):
    """Immutable fill record. Written by exchange-sync job from confirmed fills."""
    __tablename__ = "trade_logs"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    order_id = Column(UUID(as_uuid=True), ForeignKey("orders.id", ondelete="SET NULL"), nullable=True, unique=True)
    operator_id = Column(UUID(as_uuid=True), ForeignKey("operators.id", ondelete="CASCADE"), nullable=False, index=True)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)

    exchange = Column(String(32), nullable=False)
    symbol = Column(String(32), nullable=False)
    side = Column(String(8), nullable=False)
    size = Column(Float, nullable=False)
    entry_price = Column(Float, nullable=True)    # None for closing trades
    fill_price = Column(Float, nullable=False)
    fill_at = Column(DateTime(timezone=True), nullable=False)
    fee = Column(Float, nullable=False, server_default="0")
    fee_currency = Column(String(16), nullable=False)
    pnl = Column(Float, nullable=True)            # Realized PnL for closing trades
    exchange_order_id = Column(String(128), nullable=False)
    is_paper = Column(Boolean, nullable=False, server_default="false")
    verified_at = Column(DateTime(timezone=True), nullable=True)

    created_at = Column(DateTime(timezone=True), nullable=False, server_default=func.now())

    # Relationships
    order = relationship("Order", back_populates="fill")

    __table_args__ = (
        Index("ix_trade_logs_operator_fill", "operator_id", "fill_at"),
        Index("ix_trade_logs_user_fill", "user_id", "fill_at"),
    )


class Position(Base):
    """Current open position for an operator on an exchange."""
    __tablename__ = "positions"

    operator_id = Column(UUID(as_uuid=True), ForeignKey("operators.id", ondelete="CASCADE"), nullable=False)
    symbol = Column(String(32), nullable=False)
    exchange = Column(String(32), nullable=False)

    size = Column(Float, nullable=False)           # Positive = long, negative = short
    avg_entry_price = Column(Float, nullable=False)
    current_price = Column(Float, nullable=True)
    unrealized_pnl = Column(Float, nullable=True)
    unrealized_pnl_pct = Column(Float, nullable=True)
    updated_at = Column(DateTime(timezone=True), nullable=False, server_default=func.now(), onupdate=func.now())

    __table_args__ = (
        # Composite PK: one position record per (operator, symbol, exchange)
        __import__("sqlalchemy").PrimaryKeyConstraint("operator_id", "symbol", "exchange"),
    )


