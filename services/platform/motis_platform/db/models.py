"""
Motis SQLAlchemy ORM — All Tables
==================================
Single source of truth for the database schema.
Alembic autogenerates migrations from these models.

Table inventory:
  Core auth:
    users                — accounts, BYOM config, platform role
    exchange_connections — encrypted exchange API keys per user

  Agent:
    agent_memories       — per-user long-term memory (FTS via tsvector)
    conversations        — conversation sessions
    conversation_messages— individual messages within a conversation

  Operators:
    operators            — operator definitions (strategy spec, risk config)
    operator_run_logs    — per-tick execution logs

  Trading:
    orders               — orders generated by operators
    trade_logs           — immutable fill records (cross-checked with exchange)
    positions            — current open positions per operator

  Marketplace:
    marketplace_listings — operator marketplace listings
    subscriptions        — user → listing subscriptions

  Arena:
    competitions         — trading competitions
    competition_entries  — operator enrolled in competition
    competition_snapshots— daily P&L snapshots per entry

  Platform:
    operator_performance_metrics — computed Sharpe/DD/WR stats
"""

from __future__ import annotations

import uuid
from datetime import datetime

from sqlalchemy import (
    ARRAY,
    Boolean,
    Column,
    DateTime,
    Float,
    ForeignKey,
    Index,
    Integer,
    SmallInteger,
    String,
    Text,
    UniqueConstraint,
)
from sqlalchemy.dialects.postgresql import JSONB, UUID
from sqlalchemy.orm import DeclarativeBase, relationship
from sqlalchemy.sql import func, text


class Base(DeclarativeBase):
    pass


def _uuid():
    return str(uuid.uuid4())


# ── Timestamps mixin ──────────────────────────────────────────────────────────

class TimestampMixin:
    created_at = Column(
        DateTime(timezone=True), nullable=False, server_default=func.now()
    )
    updated_at = Column(
        DateTime(timezone=True),
        nullable=False,
        server_default=func.now(),
        onupdate=func.now(),
    )


# ═══════════════════════════════════════════════════════════════════════════════
# CORE AUTH
# ═══════════════════════════════════════════════════════════════════════════════

class User(TimestampMixin, Base):
    """
    Platform user account.
    JWT auth is stateless — this table stores profile + model config.
    Auth itself (password hashing) is handled by the auth provider (Supabase / custom).
    """
    __tablename__ = "users"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    email = Column(String(320), unique=True, nullable=False, index=True)
    display_name = Column(String(100), nullable=True)
    avatar_url = Column(Text, nullable=True)

    # Platform role: 'user' | 'operator_creator' | 'admin'
    role = Column(String(32), nullable=False, server_default="user")

    # BYOM — Bring Your Own Model config (encrypted at rest)
    # Encrypted via Fernet using ENCRYPTION_KEY env var
    model_base_url_enc = Column(Text, nullable=True)
    model_api_key_enc = Column(Text, nullable=True)
    model_name = Column(String(120), nullable=True)
    # Comma-separated additional model names for MoA
    model_reference_models = Column(Text, nullable=True)

    # Platform defaults (used when user hasn't configured BYOM)
    use_platform_model = Column(Boolean, nullable=False, server_default="true")

    # Billing
    stripe_customer_id = Column(String(64), nullable=True, unique=True)
    subscription_tier = Column(String(32), nullable=False, server_default="free")

    # Feature flags per user (overrides)
    feature_flags = Column(JSONB, nullable=False, server_default="{}")

    # Relationships
    exchange_connections = relationship("ExchangeConnection", back_populates="user", cascade="all, delete-orphan")
    operators = relationship("Operator", back_populates="user", cascade="all, delete-orphan")
    memories = relationship("AgentMemory", back_populates="user", cascade="all, delete-orphan")
    conversations = relationship("Conversation", back_populates="user", cascade="all, delete-orphan")


class ExchangeConnection(TimestampMixin, Base):
    """
    Encrypted exchange API key for a user.
    API key never leaves this table unencrypted — decrypted at MCP layer only.
    """
    __tablename__ = "exchange_connections"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)
    exchange = Column(String(32), nullable=False)   # Exchange enum value
    label = Column(String(64), nullable=False, server_default="default")

    # Encrypted credentials (Fernet)
    api_key_enc = Column(Text, nullable=False)
    api_secret_enc = Column(Text, nullable=False)
    api_passphrase_enc = Column(Text, nullable=True)  # OKX / some exchanges require this

    # Testnet vs mainnet
    is_testnet = Column(Boolean, nullable=False, server_default="false")

    # Verified: platform has confirmed the key works
    verified_at = Column(DateTime(timezone=True), nullable=True)
    last_sync_at = Column(DateTime(timezone=True), nullable=True)

    # Relationships
    user = relationship("User", back_populates="exchange_connections")

    __table_args__ = (
        UniqueConstraint("user_id", "exchange", "label", name="uq_exchange_connection"),
    )


# ═══════════════════════════════════════════════════════════════════════════════
# AGENT MEMORY
# ═══════════════════════════════════════════════════════════════════════════════

class AgentMemory(TimestampMixin, Base):
    """
    Per-user long-term memory store.
    Replaces Hermes's SQLite/filesystem MEMORY.md.
    FTS via ts_vector GENERATED column (PostgreSQL 12+).
    """
    __tablename__ = "agent_memories"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False)

    content = Column(Text, nullable=False)
    # 'general' | 'strategy' | 'risk_pref' | 'agent_insight' | 'operator_note'
    type = Column(String(32), nullable=False, server_default="general")
    tags = Column(ARRAY(Text), nullable=False, server_default="{}")
    source = Column(String(32), nullable=False, server_default="agent")  # 'agent' | 'user' | 'operator'
    # 1 (low) – 10 (high). Used to rank memories in context block.
    importance = Column(SmallInteger, nullable=False, server_default="5")

    # Relationships
    user = relationship("User", back_populates="memories")

    __table_args__ = (
        Index("ix_agent_memories_user_id", "user_id"),
        Index("ix_agent_memories_type", "user_id", "type"),
        # FTS index created in migration (GENERATED column + GIN index)
    )


