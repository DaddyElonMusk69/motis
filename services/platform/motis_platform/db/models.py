class Operator(TimestampMixin, Base):
    """
    A trading strategy or research/backtest operator.
    Maps 1:1 to Pydantic OperatorModel in shared package.
    """
    __tablename__ = "operators"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)
    name = Column(String(120), nullable=False)
    description = Column(Text, nullable=False, server_default="")

    # 'draft' | 'backtested' | 'paper' | 'live' | 'paused' | 'archived' | 'complete'
    state = Column(String(32), nullable=False, server_default="draft", index=True)
    version = Column(Integer, nullable=False, server_default="1")

    # 'live_trade' | 'paper_trade' | 'backtest' | 'research'
    operator_type = Column(String(32), nullable=False)

    # Strategy metadata (denormalised for fast listing queries)
    asset_class = Column(String(32), nullable=False)
    strategy_style = Column(String(32), nullable=True)
    asset_universe = Column(ARRAY(Text), nullable=False, server_default="{}")
    exchange = Column(String(32), nullable=True)

    # Full config stored as JSONB (risk_config, trigger_config, model_config_override)
    risk_config = Column(JSONB, nullable=False, server_default="{}")
    trigger_config = Column(JSONB, nullable=False, server_default="{}")
    model_config_override = Column(JSONB, nullable=True)

    # Operator graph stored externally (S3/R2) — URL reference only
    graph_spec_url = Column(Text, nullable=True)

    # Raw LangGraph code stored inline for small operators (< 64KB)
    graph_code = Column(Text, nullable=True)

    # Linked exchange connection (for live operators)
    exchange_connection_id = Column(UUID(as_uuid=True), ForeignKey("exchange_connections.id", ondelete="SET NULL"), nullable=True)

    # Capital allocation
    allocated_capital_usd = Column(Float, nullable=True)

    # Performance cache (updated by async compute job)
    cached_pnl_pct = Column(Float, nullable=True)
    cached_pnl_usd = Column(Float, nullable=True)
    cached_sharpe = Column(Float, nullable=True)
    cached_max_dd_pct = Column(Float, nullable=True)
    metrics_computed_at = Column(DateTime(timezone=True), nullable=True)

    # For ResearchOperator: S3 URL to the completed brief PDF/MD
    report_url = Column(Text, nullable=True)

    # Marketplace: if this operator is a clone from a listing
    source_listing_id = Column(UUID(as_uuid=True), nullable=True)

    # Relationships
    user = relationship("User", back_populates="operators")
    run_logs = relationship("OperatorRunLog", back_populates="operator", cascade="all, delete-orphan")
    orders = relationship("Order", back_populates="operator")
    performance_metrics = relationship("OperatorPerformanceMetrics", back_populates="operator", uselist=False, cascade="all, delete-orphan")


class OperatorRunLog(Base):
    """
    Per-tick execution log for an operator.
    Immutable — never updated, only appended.
    """
    __tablename__ = "operator_run_logs"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    operator_id = Column(UUID(as_uuid=True), ForeignKey("operators.id", ondelete="CASCADE"), nullable=False, index=True)
    # LangGraph node that produced this log
    node_name = Column(String(64), nullable=False)
    # 'info' | 'warning' | 'error' | 'signal' | 'order' | 'fill'
    level = Column(String(16), nullable=False, server_default="info")
    message = Column(Text, nullable=False)
    data = Column(JSONB, nullable=True)  # Structured payload (e.g. signal details)
    created_at = Column(DateTime(timezone=True), nullable=False, server_default=func.now())

    # Relationships
    operator = relationship("Operator", back_populates="run_logs")

    __table_args__ = (
        Index("ix_run_logs_operator_created", "operator_id", "created_at"),
    )


