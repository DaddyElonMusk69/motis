class Conversation(TimestampMixin, Base):
    """Chat session between user and the master agent."""
    __tablename__ = "conversations"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    user_id = Column(UUID(as_uuid=True), ForeignKey("users.id", ondelete="CASCADE"), nullable=False, index=True)
    title = Column(String(255), nullable=True)   # Agent-generated title after first turn
    message_count = Column(Integer, nullable=False, server_default="0")

    # Relationships
    user = relationship("User", back_populates="conversations")
    messages = relationship("ConversationMessage", back_populates="conversation", cascade="all, delete-orphan")


class ConversationMessage(Base):
    """
    Individual message in a conversation.
    Stores role + content. Tool calls stored in tool_calls JSONB.
    NOT TimestampMixin — use created_at only (messages are immutable).
    """
    __tablename__ = "conversation_messages"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    conversation_id = Column(UUID(as_uuid=True), ForeignKey("conversations.id", ondelete="CASCADE"), nullable=False, index=True)
    # 'user' | 'assistant' | 'tool'
    role = Column(String(16), nullable=False)
    content = Column(Text, nullable=True)
    # OpenAI tool_calls array if role='assistant', tool_call_id if role='tool'
    tool_calls = Column(JSONB, nullable=True)
    tool_call_id = Column(String(64), nullable=True)
    # Position in conversation
    sequence = Column(Integer, nullable=False)
    created_at = Column(DateTime(timezone=True), nullable=False, server_default=func.now())

    # Relationships
    conversation = relationship("Conversation", back_populates="messages")

    __table_args__ = (
        Index("ix_conv_msg_convo_seq", "conversation_id", "sequence"),
    )


